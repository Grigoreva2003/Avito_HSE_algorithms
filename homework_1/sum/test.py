import pytest
from main import max_even_sum   # Импорт вашей функции (поместите её в solution.py)

@pytest.mark.parametrize(
    "arr, expected",
    [
        ([1, 2, 3, 4], 10),        # сумма 10 (чётная)
        ([1, 3, 5], 8),            # сумма 9, вычитаем 1 -> 8
        ([1, 1, 1], 2),            # сумма 3, вычитаем 1 -> 2
        ([7], 0),                  # нечётное, результата нет
        ([8], 8),                  # одно чётное число
        ([5, 5, 5, 5], 20),        # сумма 20 (чётная)
        ([2, 2, 2], 6),            # сумма 6 (чётная)
        ([99, 99, 99], 198),       # сумма 297, вычитаем 99 -> 198
    ]
)
def test_max_even_sum(arr, expected):
    assert max_even_sum(arr) == expected
